"use strict"

// While-Schleife
console.log("\nWhile-Schleife:")
let counter1 = 0

while (counter1 < 5) {
    counter1++
    console.log("Counter1: ", counter1)
}

// For-Schleife
console.log("\nFor-Schleife:")
for (let counter2 = 0; counter2 < 11; counter2++) {
    console.log("Counter2: ", counter2)
}