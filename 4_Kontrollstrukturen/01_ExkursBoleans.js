"Use strict"
let greeting = "Hallo Welt"

// Exkurs Boleans: Variablen, die true oder false enthalten
console.log("\nVergleiche:")
let a = (greeting + "!") === "Hallo Welt!"
let b = (greeting + "!") !== "Hallo Welt!"

console.log("a: ", a)
console.log("b: ", b)

// Mathematische Vergleiche
console.log("\nMathematische Vergleiche:")
let zahl1 = 5
let zahl2 = 6

console.log("zahl1 === zahl2: ", zahl1 === zahl2)
console.log("zahl1 !== zahl2: ", zahl1 !== zahl2)
console.log("zahl1 < zahl2: ", zahl1 < zahl2)
console.log("zahl1 <= zahl2: ", zahl1 <= zahl2)
console.log("zahl1 > zahl2: ", zahl1 > zahl2)
console.log("zahl1 >= zahl2: ", zahl1 >= zahl2)